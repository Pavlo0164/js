//Задача 8. Користувач загадує число від 1 до 100. За 3 спроби комп’ютер намагається вгадати число користувача (використати confirm).
for (let i = 0; i < 3; i++) {
  let num = 1 + Math.floor(Math.random()*100)
  if(confirm(`Це цисло ${num}?`)){
    alert(`Клас.Я вгадав твоє число`)
    break
  }
}

