let num1 = parseInt(prompt("Введіть перше число!"));
let num2 = parseInt(prompt("Введіть друге число!"));
let sum = 0;
let i = num1 + 1;
// for (; i < num2; i++) {
//   if (i % 2) sum += i;
// }
while (i < num2) {
  if (i % 2) sum += i;
  i++
}
alert(
  `Сума непарних чисел в проміжку від ${num1} до ${num2} становить : ${sum}`
);
